#!/usr/bin/env python3
"""
This module provides a function to convert a float number to
its string representation.
It demonstrates the use of type annotations in Python.
"""


def to_str(n: float) -> str:
    """
    Convert a float to its string representation.

    Args:
        n (float): The floating-point number to convert.

    Returns:
        str: The string version of the float.
    """

    return str(n)
