#!/usr/bin/env python3
"""
This module provides a function to compute the sum of two float numbers.
It is intended to demonstrate the use of the type annotations in Python.
"""


def add(a: float, b: float) -> float:
    """
    Return the sum of two float numbers.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
